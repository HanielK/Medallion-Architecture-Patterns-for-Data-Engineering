    def _clean_name(self, name: str) -> str:
        """
        Cleans messy names for dashboard + join friendliness.
        - Removes leading commas: ",Gallant" -> "Gallant"
        - Normalizes curly apostrophes: "Jacksons’ Gap" -> "Jacksons' Gap"
        - Normalizes whitespace
        """
        if name is None:
            return None

        n = name.strip()

        # Remove leading commas (sometimes in source list)
        n = n.lstrip(",").strip()

        # Normalize curly apostrophes and similar punctuation to ASCII
        n = (n.replace("’", "'")
              .replace("‘", "'")
              .replace("“", '"')
              .replace("”", '"'))

        # Collapse multiple spaces into one
        n = " ".join(n.split())

        # OPTIONAL: If you want to remove apostrophes entirely (commented out)
        # n = n.replace("'", "")

        return 
    
    def to_spark_df(self, spark, random_100: bool = False):
        """
        Returns locations as a Spark DataFrame with schema:
        location_id, location_name, latitude, longitude

        Usage:
          loc_df = Location(random_100=True).to_spark_df(spark)
          loc_df.createOrReplaceTempView("v_locations")
        """
        rows = self.get_location_list(random_100)

        data = [
            (r.Id, r.Name, float(r.Latitude), float(r.Longitude))
            for r in rows
        ]

        return spark.createDataFrame(
            data,
            ["location_id", "location_name", "latitude", "longitude"]
        )

  
